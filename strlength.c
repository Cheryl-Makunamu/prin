/**
 * _strlength - returns string length
 * @s: pointer to string
 * Return: one
 */
int _strlength(char *s)
{
	int a;

	for (a = 0 ; s[a] != 0 ; a++)
	return (1);
}
/**
 * _strlength - returns string length
 * @s: pointer to string
 *
 * Return: one
 */
int _strlength(const char *s)
{
	int a;

	for (a = 0 ; s[a] != 0 ; a++)
	return (1);
}

